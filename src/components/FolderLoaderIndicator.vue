<template>
  <div class="vuefinder__folder_loader_indicator">

    <LoadingSVG v-if="loading" class="vuefinder__folder_loader_indicator--loading_icon"/>
    <div class="vuefinder__folder_loader_indicator--loaded" v-else>
      <SquareMinusSVG class="vuefinder__folder_loader_indicator__minus-icon" v-if="opened && getLoadedFolder()?.folders.length" />
      <SquarePlusSVG class="vuefinder__folder_loader_indicator__plus-icon" v-if="!opened" />
    </div>

  </div>
</template>

<style>
.vuefinder__folder_loader_indicator {
  @apply h-5 w-5 shrink-0;
}

.vuefinder__folder_loader_indicator--loading_icon {
  @apply p-1;
}

.vuefinder__folder_loader_indicator--loaded {
  @apply cursor-pointer;
}

.vuefinder__folder_loader_indicator__minus-icon {
  @apply text-gray-600;
}

.vuefinder__folder_loader_indicator__plus-icon {
  @apply text-gray-400;
}
</style>


<script setup>
import {ref, inject, defineProps, watch} from 'vue';

import SquarePlusSVG from "./icons/plus.svg";
import SquareMinusSVG from "./icons/minus.svg";
import LoadingSVG from "./icons/loading.svg";
import upsert from "../utils/upsert";

const props = defineProps({
  adapter: {
    type: String,
    required: true,
  },
  path: {
    type: String,
    required: true,
  }
});

const app = inject('ServiceContainer');
const {t} = app.i18n;
const opened = defineModel();
const loading = ref(false)

// loading..

watch(() => opened.value, () =>
    getLoadedFolder()?.folders.length || fetchSubFolders()
);

function toggleIndicator() {
  return opened.value = !opened.value;
}

function getLoadedFolder() {
  return app.treeViewData.find(e => e.path === props.path) ;
}

const fetchSubFolders = () => {
  loading.value = true;
  app.requester.send({
    url: '',
    method: 'get',
    params: {
      q: 'subfolders',
      adapter: props.adapter,
      path: props.path,
    },
  })
      .then(data => {
        upsert(app.treeViewData, {path: props.path, ...data})
      })
      .catch((e) => {
      })
      .finally(() => {
        loading.value = false;
      });

}
</script>

